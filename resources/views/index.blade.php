                <h2>Regels</h2>
                <p>
                    1. Vermeld uw bronnen
        Het is zeer waarschijnlijk dat u op een bepaald moment tijdens het schrijven of bloggen wilt verwijzen naar een artikel of blogbericht dat u online leest.
        
        Hoewel het mogelijk is om een ​​zin of een paar woorden te kopiëren zonder de auteursrechtwetten te schenden, moet je, om binnen de regels van redelijk gebruik te blijven, de bron vermelden waar dat citaat vandaan kwam.
        
        U moet dit doen door de naam van de oorspronkelijke auteur en de naam van de website of blog te vermelden waar het citaat oorspronkelijk werd gebruikt, samen met een link naar de oorspronkelijke bron.
                </p>
                <p>
                    2. Maak betaalde aanbevelingen bekend
        Bloggers moeten open en eerlijk zijn over alle betaalde aanbevelingen. Als u wordt betaald om een ​​product te gebruiken en te beoordelen of te promoten, moet u dit onthullen.
        
        De Federal Trade Commission, die de waarheid in advertenties reguleert, publiceert een uitgebreide FAQ over dit onderwerp. Dit zijn de basisprincipes:
        
        Label inhoud die reclame is duidelijk.
        Maak gelieerde ondernemingen bekend. Label links die uw lezers naar uw filialen leiden, of bouw een pagina die uw filialen en relaties met hen uitlegt.
        Doe niet alsof u een objectieve derde partij bent als u dat niet bent. Als u voor een bedrijf werkt, vermeld dat feit dan in alle inhoud die naar het bedrijf of zijn producten of diensten verwijst.
                </p>
                <p>
                    
            3 Vraag toestemming 
            Terwijl het citeren van een paar woorden of een zin en het vermelden van uw bron acceptabel is onder de wetten inzake redelijk gebruik, zijn de wetten inzake redelijk gebruik met betrekking tot online-inhoud nog steeds een grijs gebied in rechtszalen. 
            
            Als je van plan bent om meer dan een paar woorden of zinsdelen te kopiëren, kun je het beste voorzichtig zijn en de oorspronkelijke auteur om toestemming vragen om hun woorden - met de juiste bronvermelding - opnieuw op je blog te publiceren. Pleeg nooit plagiaat. 
            
            Toestemming vragen geldt ook voor het gebruik van foto's en afbeeldingen op je blog. Tenzij een foto of afbeelding die je wilt gebruiken afkomstig is van een bron die je duidelijk toestemming geeft om deze op je blog te gebruiken, moet je de oorspronkelijke fotograaf of ontwerper om toestemming vragen om de foto of afbeelding met de juiste bronvermelding op je blog te gebruiken.
                </p>
                <p>
                   
            4 Publiceer een privacybeleid 
            Privacy is een zorg voor de meeste mensen op internet. U dient een privacybeleid te publiceren en u eraan te houden. Het kan zo simpel zijn als "YourBlogName zal je e-mailadres nooit verkopen, verhuren of delen." 
        
            Mogelijk hebt u een volledige pagina nodig voor dit bericht, afhankelijk van hoeveel informatie u van uw lezers verzamelt.
                </p>
                <p>
                   
            5 Speel leuk 
            Dat je blog van jou is, wil nog niet zeggen dat je vrij spel hebt om alles te schrijven wat je maar wilt zonder repercussies. De inhoud op uw blog is beschikbaar voor de wereld om te zien. 
        
            Net zoals de geschreven woorden van een verslaggever of de mondelinge verklaringen van een persoon als smaad of laster kunnen worden beschouwd, kunnen de woorden die u op uw blog gebruikt, dat ook. 
        
            Vermijd juridische verwikkelingen door te schrijven met een wereldwijd publiek in gedachten. Je weet nooit wie er op je blog terechtkomt. 
        
            Als je blog reacties accepteert, reageer er dan bedachtzaam op. Ga niet in discussie met je lezers.
                </p>
                <p>
                    6 Corrigeer fouten
                    Als u vindt dat u onjuiste informatie hebt gepubliceerd, verwijder het bericht dan niet gewoon. Corrigeer het en leg de fout uit. Uw lezers zullen uw eerlijkheid waarderen.
                </p>
        
